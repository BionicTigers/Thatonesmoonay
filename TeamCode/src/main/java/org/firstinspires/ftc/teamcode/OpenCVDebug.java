@Autonomous(name="Debug OpenCV", group ="test")
public class OpenCVDebug extends LinearOpMode {



    @Override public void runOpMode() {

        SamplingOrderDetector detector = new SamplingOrderDetector();
        detector.init(hardwareMap.appContext,CameraViewDisplay.getInstance(),0,false);
        detector.useDefaults();

        detector.downscale = 0.4; // How much to downscale the input frames

        // Optional Tuning
        detector.areaScoringMethod = DogeCV.AreaScoringMethod.MAX_AREA; // Can also be PERFECT_AREA
        //detector.perfectAreaScorer.perfectArea = 10000; // if using PERFECT_AREA scoring
        detector.maxAreaScorer.weight = 0.001;

        detector.ratioScorer.weight = 15;
        detector.ratioScorer.perfectRatio = 1.0;

        waitForStart();

        detector.enable();

        while(opModeIsActive()) {

            telemetry.addData("cubePos",((SamplingOrderDetector) detector).getCurrentOrder());
            telemetry.update();

        }
    }

}
